      driver_card,
      driver_revenue,
      driver_incentive_raw,
      driver_incentive_rpc_raw,
      driver_incentive_rpc_sum,
      driver_incentive_rpc_financial_sum,
      status
    ) SELECT
      tv.operator_id,
      tv.start_territory_id,
      tv.end_territory_id,
      tv.applied_policies,
      tv.journey_id,
      tv.trip_id,
      tv.journey_start_datetime,
